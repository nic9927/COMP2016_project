CREATE TABLE Students(
	sno CHAR(10),
	name CHAR(20),
	gender CHAR(1),
	major CHAR(20),
PRIMARY KEY (sno));

CREATE TABLE Books(
	call_no CHAR(10),
	ISBN CHAR(10),
	title CHAR(20),
	author CHAR(20),
	amount INTEGER,
	location CHAR(10),
	UNIQUE (call_no),
	PRIMARY KEY (ISBN));




CREATE TABLE Borrow(
	b_date DATE,
	d_date DATE,
	book CHAR(20),
	borrower CHAR(10),
	PRIMARY KEY (book, borrower),
	FOREIGN KEY (book) REFERENCES Books(call_no),
	FOREIGN KEY (borrower) REFERENCES Students(sno));


CREATE TABLE Reserve(
	sno CHAR(10),
	book CHAR(20),
	reserveDate DATE
	PRIMARY KEY (sno),
FOREIGN KEY (sno) REFERENCES Student,
	FOREIGN KEY (book) REFERENCES Books(call_no));

CREATE TABLE Renew(
	sno CHAR(10),
	Book CHAR(20),
	PRIMARY KEY (sno, book)
	FOREIGN KEY (sno) REFERENCES Student,
	FOREIGN KEY (book) REFERENCES Books(call_no));

